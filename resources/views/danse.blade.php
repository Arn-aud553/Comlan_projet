    <h1 class="content-title">Danse Béninoise</h1>
    
    <p class="content-text">
        La danse occupe une place centrale dans l'identité culturelle béninoise. Depuis les royaumes du Dahomey jusqu'aux communautés actuelles, elle est à la fois un langage, une mémoire et un rituel qui accompagne chaque moment important de la vie : naissance, initiation, mariage, récolte, funérailles ou festivités royales.
    </p>
    
    <p class="content-text">
        Au sud comme au nord, chaque ethnie possède ses propres danses, chacune portant une symbolique forte. Parmi les plus connues, on retrouve :
    </p>
    
    <ul class="content-list">
        <li><strong>Zangbéto</strong> : danse mystérieuse des gardiens de la nuit, exécutée avec des masques tournoyants.</li>
        <li><strong>Adjogan</strong> : danse royale du Danxomè, élégante et rythmée par les hochets gankéké.</li>
        <li><strong>Agbadja</strong> : danse festive du peuple Fon, pratiquée lors des célébrations et des rencontres communautaires.</li>
        <li><strong>Tchinkoun</strong> : danse énergique composée de mouvements rapides et acrobatiques.</li>
        <li><strong>Sakpata</strong> : danse dédiée au vodoun de la terre, riche en gestuelle symbolique.</li>
        <li><strong>Guèlèdè</strong> : danse masquée exécutée pour honorer les mères et apaiser les forces spirituelles.</li>
        <li><strong>Yoruba Gèlèdé, Bariba Baatombu, Nagot, Dendi, Wémè</strong> : plusieurs danses régionales liées aux traditions ancestrales.</li>
    </ul>
    
    <p class="content-text">
        La danse béninoise est donc un pont entre le sacré et le social. Elle continue aujourd'hui d'inspirer chorégraphes, festivals et créations modernes tout en préservant les valeurs du pays.
    </p>
