    <h1 class="content-title">Musique Béninoise</h1>
    
    <p class="content-text">
        La musique béninoise est l'une des plus riches d'Afrique de l'Ouest. Héritée des traditions vodoun, des chants royaux et des musiques de travail, elle a évolué pour devenir un mélange unique entre rythmes ancestraux et influences modernes.
    </p>
    
    <p class="content-text">
        Au fil du temps, plusieurs rythmes ont marqué la culture :
    </p>
    
    <ul class="content-list">
        <li><strong>Tchinkoumé</strong> : rythme traditionnel du sud, très utilisé dans les cérémonies vodoun.</li>
        <li><strong>Sato</strong> : rythme majestueux joué sur un grand tambour sacré.</li>
        <li><strong>Agbadja</strong> : musique de fête accompagnée d'un ensemble de tambours.</li>
        <li><strong>Zinli</strong> : chant mélodieux utilisé pour transmettre des messages communautaires.</li>
        <li><strong>Rara</strong> : rythme festif inspiré des processions vodoun.</li>
        <li><strong>Houn-Noukoun</strong> : percussion sacrée dédiée aux divinités.</li>
        <li><strong>Kpanlogo, Massé, Gbekeke, Wémè</strong> : rythmes régionaux aux fonctions sociales variées.</li>
    </ul>
    
    <p class="content-text">
        À partir des années 1960, ces rythmes ont donné naissance à de nouveaux styles comme :
    </p>
    
    <ul class="content-list">
        <li><strong>Le Tchink System</strong> : popularisé par Sagbohan Danialou.</li>
        <li><strong>Le "Bénin Afrobeat"</strong> : fusion portée par des artistes comme Angélique Kidjo.</li>
        <li><strong>Le modern folk béninois</strong> : qui mêle guitare, tambours et chants traditionnels.</li>
    </ul>
    
    <p class="content-text">
        Aujourd'hui encore, la musique béninoise accompagne les cérémonies, les festivals et les scènes internationales, faisant rayonner la culture du pays.
    </p>
